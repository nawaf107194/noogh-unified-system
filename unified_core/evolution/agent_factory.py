"""
Agent Factory — Autonomous Agent Generation System v1.0
=======================================================

Enables the evolution pipeline to create NEW agents autonomously.

Architecture:
  1. AgentFactory analyzes system gaps (missing capabilities)
  2. Uses the Brain (Teacher LLM) to design an agent spec
  3. Generates agent code from the spec
  4. Validates: syntax, contract compliance, safety
  5. Deploys to agents/ directory
  6. Registers with AgentRole and MessageBus

Safety Layers:
  - Generated code is sandboxed and validated via AST
  - No network, file-write, or system-exec capabilities by default
  - Policy Gate reviews before deployment
  - All agents must inherit from AgentWorker
"""

