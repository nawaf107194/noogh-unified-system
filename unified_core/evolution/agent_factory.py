# ============================================================
#  Agent Blueprint — Design spec before code generation
# ============================================================

@dataclass
class AgentBlueprint:
    """Blueprint for a new agent before code generation."""
    agent_id: str
    name: str              # e.g. "HealthMonitorAgent"
    role: str              # e.g. "health_monitor"
    description: str       # What does this agent do?
    capabilities: List[str]  # What capabilities it handles
    handlers: List[Dict[str, str]]  # [{name, description, capability}]
    risk_level: str = "SAFE"  # SAFE | RESTRICTED | DANGEROUS
    
    # Metadata
    reason: str = ""       # Why was this agent needed?
    created_at: float = field(default_factory=time.time)
    generated_code: str = ""
    validation_result: Optional[Dict] = None


