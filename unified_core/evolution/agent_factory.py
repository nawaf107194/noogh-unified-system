    def __init__(self):
        self.agents_dir = Path(__file__).parent.parent.parent / "agents"
        self.agents_dir.mkdir(exist_ok=True)
        self._generated_agents = []
        self._registry_path = self.agents_dir / "agent_registry.json"
        self._load_registry()
        logger.info(f"🏭 AgentFactory initialized — agents dir: {self.agents_dir}")
    
    def _load_registry(self):
        """Load the registry of generated agents."""
        if self._registry_path.exists():
            try:
                with open(self._registry_path, 'r') as f:
                    self._generated_agents = json.load(f)
            except Exception:
                self._generated_agents = []
        else:
            self._generated_agents = []
    
    def _save_registry(self):
        """Save the registry of generated agents (deduplicates by role)."""
        # Defense-in-depth: deduplicate by role, keeping the latest entry
        seen_roles = {}
        for agent in self._generated_agents:
            role = agent.get("role", "unknown")
            seen_roles[role] = agent  # Last one wins (most recent)
        self._generated_agents = list(seen_roles.values())
        
        with open(self._registry_path, 'w') as f:
            json.dump(self._generated_agents, f, indent=2, default=str)
    
    # ─── Main API ──────────────────────────────────────────
    
    def generate_agent(self, blueprint_id: str = None, 
                        blueprint: AgentBlueprint = None) -> Dict[str, Any]:
        """
        Generate a new agent from a blueprint.
        
        Args:
            blueprint_id: ID of a builtin blueprint (e.g. "health_monitor")
            blueprint: Custom AgentBlueprint object
            
        Returns:
            {"success": bool, "agent_file": str, "class_name": str, ...}
        """
        # Get blueprint
        if blueprint is None:
            if blueprint_id not in self.BUILTIN_BLUEPRINTS:
                return {"success": False, "error": f"Unknown blueprint: {blueprint_id}"}
            blueprint = self.BUILTIN_BLUEPRINTS[blueprint_id]
        
        # Check if already generated
        agent_file = self.agents_dir / f"{blueprint.role}_agent.py"
        if agent_file.exists():
            return {
                "success": False, 
                "error": f"Agent already exists: {agent_file}",
                "hint": "Delete the file first or use a different role"
            }
        
        # Step 1: Generate code from blueprint
        code = self._generate_code(blueprint)
        
        # Step 2: Validate the generated code
        validation = self._validate_agent_code(code, blueprint)
        if not validation["valid"]:
            return {
                "success": False, 
                "error": "Validation failed",
                "violations": validation["violations"]
            }
        
        # Step 3: Write the agent file
        with open(agent_file, 'w', encoding='utf-8') as f:
            f.write(code)
        
        # Step 4: Register in the AgentRole enum (dynamic)
        self._register_role(blueprint.role)
        
        # Step 5: Update registry
        entry = {
            "agent_id": blueprint.agent_id,
            "name": blueprint.name,
            "role": blueprint.role,
            "file": str(agent_file),
            "capabilities": blueprint.capabilities,
            "reason": blueprint.reason,
            "created_at": time.time(),
            "validation": validation
        }
        self._generated_agents.append(entry)
        self._save_registry()
        
        logger.warning(
            f"🏭 NEW AGENT GENERATED: {blueprint.name} "
            f"({len(blueprint.capabilities)} capabilities) → {agent_file.name}"
        )
        
        return {
            "success": True,
            "agent_file": str(agent_file),
            "class_name": blueprint.name,
            "role": blueprint.role,
            "capabilities": blueprint.capabilities,
            "validation": validation
        }
    
    # ─── Code Generation ──────────────────────────────────
    
    def _generate_code(self, bp: AgentBlueprint) -> str:
        """Generate Python code for an agent from blueprint."""
        
        # Build capabilities doc
        capabilities_doc = "\n".join(
            f"    - {cap}" for cap in bp.capabilities
        )
        
        # Build handler registrations
        registrations = []
        for h in bp.handlers:
            registrations.append(
                f'            "{h["capability"]}": self.{h["name"]},'
            )
        handler_registrations = "\n".join(registrations)
        
        # Build handler methods
        methods = []
        for h in bp.handlers:
            body = h.get("body", '            return {"success": True, "message": "Not implemented"}')
            method = HANDLER_TEMPLATE.format(
                method_name=h["name"],
                description=h["description"],
                capability=h["capability"],
                handler_body=body
            )
            methods.append(method)
        handler_methods = "\n".join(methods)
        
        # Build module name
        module_name = bp.role.lower().replace(" ", "_")
        
        code = AGENT_TEMPLATE.format(
            class_name=bp.name,
            description=bp.description,
            timestamp=time.strftime("%Y-%m-%d %H:%M:%S"),
            reason=bp.reason,
            module_name=module_name,
            capabilities_doc=capabilities_doc,
            handler_registrations=handler_registrations,
            role=bp.role,
            handler_methods=handler_methods
        )
        
        return code
    
    # ─── Validation ────────────────────────────────────────
    
    def _validate_agent_code(self, code: str, bp: AgentBlueprint) -> Dict[str, Any]:
        """
        Validate generated agent code for safety and contract compliance.
        
        Checks:
        1. Syntax (AST parse)
        2. Contract compliance (inherits AgentWorker, has __init__)
        3. Safety (no dangerous imports, no eval, no exec)
        4. No network calls, no file writes without approval
        """
        violations = []
        
        # 1. Syntax check
        try:
            tree = ast.parse(code)
        except SyntaxError as e:
            return {"valid": False, "violations": [f"Syntax error: {e}"]}
        
        # 2. Contract compliance: must have a class inheriting AgentWorker
        classes = [n for n in ast.walk(tree) if isinstance(n, ast.ClassDef)]
        agent_class = None
        for cls in classes:
            for base in cls.bases:
                if isinstance(base, ast.Name) and base.id == "AgentWorker":
                    agent_class = cls
                    break
                elif isinstance(base, ast.Attribute) and base.attr == "AgentWorker":
                    agent_class = cls
                    break
        
        if agent_class is None:
            violations.append("No class inheriting AgentWorker found")
        else:
            # Must have __init__
            has_init = any(
                isinstance(n, ast.FunctionDef) and n.name == "__init__"
                for n in ast.walk(agent_class)
            )
            if not has_init:
                violations.append("Agent class missing __init__")
            
            # Must call super().__init__
            has_super = "super().__init__" in code
            if not has_super:
                violations.append("Agent __init__ must call super().__init__()")
        
        # 3. Safety: banned patterns
        banned_imports = {"subprocess", "shutil", "ctypes", "socket"}
        # Allow subprocess only for RESTRICTED+ agents
        if bp.risk_level == "SAFE":
            for node in ast.walk(tree):
                if isinstance(node, ast.Import):
                    for alias in node.names:
                        if alias.name.split(".")[0] in banned_imports:
                            violations.append(f"Banned import for SAFE agent: {alias.name}")
                elif isinstance(node, ast.ImportFrom):
                    if node.module and node.module.split(".")[0] in banned_imports:
                        violations.append(f"Banned import for SAFE agent: {node.module}")
        
        # No eval/exec
        for node in ast.walk(tree):
            if isinstance(node, ast.Call):
                if isinstance(node.func, ast.Name):
                    if node.func.id in ("eval", "exec", "__import__"):
                        violations.append(f"Dangerous function call: {node.func.id}()")
        
        # 4. Size limits
        line_count = len(code.splitlines())
        if line_count > 500:
            violations.append(f"Agent too large: {line_count} lines (max 500)")
        
        return {
            "valid": len(violations) == 0,
            "violations": violations,
            "line_count": line_count,
            "class_name": agent_class.name if agent_class else None,
            "handler_count": len(bp.handlers)
        }
    
    # ─── Role Registration ─────────────────────────────────
    
    def _register_role(self, role: str):
        """
        Dynamically register a new AgentRole.
        
        Python Enums don't support dynamic members easily,
        so we check if the role already exists before adding.
        """
        from unified_core.orchestration.messages import AgentRole
        
        # Check if role already exists
        try:
            AgentRole(role)
            logger.info(f"AgentRole '{role}' already registered")
        except ValueError:
            # Not in enum — we'll need to handle this differently
            # For now, log a warning. The agent can use string matching.
            logger.warning(
                f"AgentRole '{role}' not in Enum — "
                f"agent will use string-based role matching"
            )
    
